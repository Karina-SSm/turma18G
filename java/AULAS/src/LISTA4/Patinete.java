package LISTA4;

public class Patinete {
	public String cor;

	public int tamanho;
	public Patinete(String cor,int tamanho) {
		super();
		this.cor = cor;
		this.tamanho = tamanho;
		
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	

	public int getTamanho() {
		return tamanho;
	}
	public void setTamanho(int tamanho) {
		this.tamanho = tamanho;
	}
	
	}

	
	


